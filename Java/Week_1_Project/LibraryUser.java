public abstract class LibraryUser
{
    private String name;
    private String ID;

    //Accessors
    public String getName()
    {
        return this.name;
    }
    public String getID()
    {
        return this.ID;
    }

    //Mutators
    public void setName(String name)
    {
        this.name = name;
    }
    public void setID(String ID)
    {
        this.ID = ID;
    }



}
